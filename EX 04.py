#Make a Program that asks for the 4 bimonthly notes and shows the average.
fnot1 = float(input("Put note of first bimonthly: "))
fnot2 = float(input("Put note of second bimonthly: "))
fnot3 = float(input("Put note of third bimonthly: "))
